							<span id="mobileclinics" class="subheading">Mobile clinics</span>
							<p>When the trust began its operations in Delhi in 2003, there was one mobile unit donated by the Member of Parliament (MP) Shri Kapil Sibal. 
							Initially, the unit had two doctors and a nurse on board. In March 2007, the Ila Trust received their second mobile clinic donated by the Elton John AIDS Foundation.
							Each unit now operates with four doctors on board per visit, as well as a nurse and a medicine dispenser. </p>
							<p>The clinics go to predefined locations around old Delhi and the Okhla Industrial Area. 
							It operates six days a week with six morning sessions and three afternoon sessions.
                                                        Treatment is provided for general ailments as well as chronic diseases such as diabetes and AIDS.
                                                        The early morning sessions provide an opportunity for working people to visit doctors before their workday.
                                                        The primary objective of the afternoon sessions is to serve women and children.
							A detailed schedule of the operation is provided below. Between the two clinics, the Ila Trust treated more than 100,000 patients in 2009.
							All expenses towards treating the patients (tests, medicine and treatment) is borne by the Trust.
							</p>
							<span id="health" class="subheading">Health and Nutrition</span>
							<p>Improving the health of people means not just providing medicine, but also doing our best to improve our patients' quality of living.
                                                            Keeping the mobile units as a base, we also perform the following activities. </p>
							<ul>
								<li>Create awareness on hygiene, birth control, child care, etc. </li>
								<li>Distribute soap and shampoo where required</li>
								<li>Provide biscuits to children (improve nutrition)</li>
								<li>Distribute clothes to patients throughout the year</li>
								<li>Distribute blankets in the winter</li>
							</ul>
							<span id="emr" class="subheading">EMR</span>
							<p>We are implementing an exciting new electronic medical record (EMR) system, which will record every patient-doctor encounter.
							Once our EMR is in place we will be able to track our patient's health histories, perform follow-up, better manage our inventory,
							identify the geo-density of symptoms and diseases across the greater New Delhi region, and inform public health officials of outbreaks of diseases at times of crisis. The goal of our EMR is to improve patient care. Our EMR is currently under development and we expect to implement it in the coming months. Please check back soon for updates. </p>
