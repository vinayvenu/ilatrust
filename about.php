							<span id="background" class="subheading">Our Background</span>
							<p>The Ila Trust was founded on October 26th 1994 by Reeta Devi who began working with Mother Teresa at the age of sixteen. In 1995, Reeta started an ambulance in Guwahati, a city in northeastern India, with doctors, nurses and technicians.
                                                            A year later Reeta spearheaded the establishment of a hospice for AIDS and AIDS-related diseases in Guwahati for Mother Teresa.
                                                            In 1997, she set up her own hospice in a Boro tribal village in northeastern India.
                                                            Here she continued her work in all spheres of health care, which included HIV/AIDS.</p>
							<p>In 2003 the Ila Trust's first mobile clinic in Delhi started with two doctors and a nurse on board. 
                                                            The Ila Trust received its second mobile clinic, donated by the Elton John AIDS Foundation, in March 2007.
                                                            Currently the Ila Trust has seven doctors, two pharmacists, an office manager and two full-time volunteers.
                                                        </p>
                                                        <p>The work performed by the Ila Trust is dedicated to Mother Teresa and the missionaries of charity. </p>
							<span id="need" class="subheading">The Need </span>
                                                        <p>Falling sick in Delhi can be dangerous for the poor and under-served.
                                                            Private providers  are expensive and of variable quality.
                                                            The public health system, where present, is often overloaded and does not cover the high cost of
                                                            medications and other expensive supplies.
                                                            Many poor people, or those just making ends meet, can descend into abject poverty as a result
                                                            of falling ill. The Ila Trust seeks to take care of the poor and under-served by providing healthcare 
                                                            at no cost to the patient.
                                                        <p>Most patients will seek healthcare if they feel very sick,
                                                            but often pay no attention to symptoms if those complaints donâ€™t interfere with their daily activities.
                                                            Thus, people ignore illnesses that are asymptomatic or slow to fulminate -- illnesses that can be cured when caught early
                                                            but exact a large toll if left to fester. In India, the poor suffer such bad outcomes disproportionately because they cannot
                                                            spare time from their occupations to seek non-catastrophic care.

                                                          </p>
							<span id="need" class="subheading">Who we serve</span>
							<p>The population the Ila Trust serves is the most vulnerable in society.
                                                            They include rickshaw-wallahs, pavement dwellers, sex workers, street children and migrant daily-wage laborers.
                                                            The illnesses we treat are mostly due to malnutrition, water contamination and unsanitary living conditions,
                                                            resulting in skin diseases, tuberculosis, diabetes, abcessess and open wounds. </p>
							<span id="whatwedo" class="subheading">What we do</span>
							<p>The backbone of the Ila Trust's work is the two mobile clinics that are operated across several slums in Delhi. The clinics see patients in places from where patients can spare a
                                                            few minutes to get much-needed primary care - at their homes early in the morning before they go to work and later in the day.
                                                           
                                                            One mobile clinic works in and around old Delhi in the areas of Lahori Gate, G.B Road, Jama Masjid, Majnu Ka Tila,
                                                            Turkman Gate, Ajmeri Gate, Jamna Bazaar and Chandini Chowk. The second mobile clinic goes to Okhla Industrial Area.
                                                            The Ila Trust operates these mobile medical clinics six days a week, and treats more than 100,000 patients a year. </p>
							<p>Apart from providing free medicine and health check-ups, the Ila Trust also provides the following: <br><br></p>
							<ul>
								<li>Blood tests through external laboratories at the expense of the Trust</li>
								<li>Treatment for Tuberculosis</li>
								<li>Treatment for HIV by giving ART drugs and/or supplemental medicine</li>
								<li>Create awareness on hygiene, birth control, child care etc</li>
								<li>Distribution of condoms as required</li>
 								<li>Distribution of biscuits to children to improve nutrition</li>
 								<li>Distribution of blankets to the needy in winter</li>
 								<li>Distribution of clothes to patients throughout the year</li> 
 								<li>Distribution of shampoo to people suffering from scalp diseases</li>
								<li>Distribution of soap as and when required</li>
								<li>A Literacy centre at Sarai Khalil</li>
